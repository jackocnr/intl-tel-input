import fs from 'node:fs';
import path from 'node:path';

//* Generates fallback country-name translations for the handful of locales where
//* Chrome's filtered ICU data lacks region display names, so Intl.DisplayNames
//* silently falls back to English (e.g. Bosnian on Chrome desktop). See the docs
//* section "Country name translations" for the full explanation.
//*
//* Source of truth: Node's full-ICU Intl.DisplayNames, which has the region data
//* that Chrome's build strips out. These names match what Chrome WOULD show if it
//* shipped the data. Re-run after a Node/ICU upgrade to refresh:
//*   node scripts/generate-country-names.js
//*
//* To find which locales need this, run the check in
//* site/src/playground/js/playground.ts (hasNoBrowserCountryNameData) against a
//* given browser; add any newly-failing locale below.

const localeDir = 'packages/core/src/js/locale';
const outDir = path.join(localeDir, 'country-names');
const dataFile = path.join(localeDir, '..', 'data.ts');

//* Locales whose region display names Chrome's ICU build omits.
const FALLBACK_LOCALES = ['bs', 'hy', 'is', 'mk', 'sq', 'uz'];

//* Extract iso2 codes (in source order) from data.ts — the first element of each
//* country sub-array, e.g.   "af", // Afghanistan
const dataSrc = fs.readFileSync(dataFile, 'utf8');
const iso2s = [...dataSrc.matchAll(/^\s*"([a-z]{2})",/gm)].map((m) => m[1]);
if (iso2s.length === 0) {
  throw new Error(`No iso2 codes found in ${dataFile} — has the format changed?`);
}

fs.mkdirSync(outDir, { recursive: true });

for (const locale of FALLBACK_LOCALES) {
  const supported = Intl.DisplayNames.supportedLocalesOf([locale]);
  if (supported.length === 0) {
    throw new Error(`This Node build's ICU does not support locale "${locale}" — cannot generate fallback names. Use a full-ICU Node.`);
  }
  const dn = new Intl.DisplayNames([locale], { type: 'region' });

  const entries = [];
  const missing = [];
  for (const iso2 of iso2s) {
    const name = dn.of(iso2.toUpperCase());
    if (!name || name === iso2.toUpperCase()) {
      missing.push(iso2);
      continue;
    }
    entries.push([iso2, name]);
  }
  if (missing.length) {
    throw new Error(`Locale "${locale}" is missing region names for: ${missing.join(', ')} — this Node build lacks full ICU data.`);
  }

  const body = entries
    .map(([iso2, name]) => `  ${iso2}: ${JSON.stringify(name)},`)
    .join('\n');

  const fileContent =
    `//* THIS FILE IS AUTO-GENERATED BY scripts/generate-country-names.js. DO NOT EDIT.\n` +
    `//* Fallback country names for "${locale}", for browsers whose Intl.DisplayNames\n` +
    `//* lacks region data for this locale (e.g. Chrome desktop falls back to English).\n` +
    `import type { Iso2 } from "../../data.js";\n\n` +
    `const countryNames: Partial<Record<Iso2, string>> = {\n${body}\n};\n\n` +
    `export default countryNames;\n`;

  fs.writeFileSync(path.join(outDir, `${locale}.ts`), fileContent);
  console.log(`Generated ${locale}.ts (${entries.length} countries).`);
}

console.log(`\nDone. Fallback country names written to ${outDir} for: ${FALLBACK_LOCALES.join(', ')}.`);
